const numX = document.getElementById('num-X');
const numY = document.getElementById('num-Y');

export function sum(numX, numY) {
    return numX + numY;
  }

export function difference(numX, numY) {
    return numX - numY;
  }
  
export function quotent(numX, numY) {
    return numX / numY;
  }

export function product(numX, numY) {
    return numX * numY;
  }